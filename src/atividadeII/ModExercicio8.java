public class ModExercicio8 {
public static void main(String[] args) {
	int valor=4;
	
	switch(valor) {
	
	case 1:
		System.out.println("Domingo");
	break;
	
	
	case 2:
		System.out.println("Segunda-Feira");
	break;
	
	
	case 3:
		System.out.println("Terça-Feira");
	break;
	
	
	case 4:
		System.out.println("Terça-Feira");
	break;
	
	
	case 5:
		System.out.println("Terça-Feira");
	break;
	
	case 6:
		System.out.println("Terça-Feira");
	break;
	
	case 7:
		System.out.println("Terça-Feira");
	break;
	
	
	default:
		System.out.println("valor invalido");
	break;
	}
}
}
